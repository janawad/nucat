							  customer demand and needs will surely help business to grow. Treat your customers with respect, and always 
							  go the extra mile for them. Word of mouth is priceless--your best advertising can come from a happy customer. 
							  All it costs is your time. One should never forget that the customer is God so customer should get all services
							  before sale or after sale so as to ensure customer loyalty and future engagements.</p>
								
								
								
                              
